/** \brief Pide un numero flotante
 *
 * \param char[] Mensaje a ser mostrado
 * \return float El numero ingresado por el usuario
 *
 */
float pedirflotante(char[]);

/** \brief Recibe los 2 numeros ingresado por el usuario y los suma
 *
 * \param float Valor del 1er operando
 * \param float Valor del 2do operando
 * \return float El resultado de la suma
 *
 */
float sumar(float, float);

/** \brief Recibe los 2 numeros ingresado por el ususario y los resta
 *
 * \param float Valor del 1er operando
 * \param float Valor del 2do operando
 * \return float El resultado de la resta
 *
 */
float restar(float, float);

/** \brief Recibe los 2 numeros ingresado por el ususario y los divide
 *
 * \param float Valor del 1er operando
 * \param float Valor del 2do operando
 * \return float El resultado de la division
 *
 */
float division(float, float);

/** \brief Recibe los 2 numeros ingresado por el ususario y los multiplica
 *
 * \param float Valor del 1er operando
 * \param float Valor del 2do operando
 * \return float El resultado de la multiplicacion
 *
 */
float multiplicar(float, float);

/** \brief Recibe el 1er operando ingresado por el usuario y calcula el factorial
 *
 * \param int Valor del 1er operando
 * \return int El resultado del factorial
 *
 */
int factorial(int);
